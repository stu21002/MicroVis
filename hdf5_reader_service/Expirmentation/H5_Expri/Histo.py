def read_numbers_from_file(file_path):
    numbers = []

    # Open the file in read mode
    with open(file_path, 'r') as file:
        # Iterate through each line in the file
        for line in file:
            # Strip whitespace and convert to a float, then append to the list
            try:
                number = float(line.strip())
                numbers.append(number)
            except ValueError:
                # Handle the case where conversion fails (e.g., due to invalid data)
                print(f"Could not convert line to number: {line.strip()}")

    return numbers





